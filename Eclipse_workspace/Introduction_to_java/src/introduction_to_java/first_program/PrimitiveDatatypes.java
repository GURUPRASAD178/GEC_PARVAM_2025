package introduction_to_java.first_program;

public class PrimitiveDatatypes {
	public static void main (String []args) {
		//primitive data types
		/*
		 * 1 byte = 23  (default = 0)
		 * 2 short = 300 (default = 0)
		 * 3 int = 3000 (default = 0)
		 * 4 long = 300L (default = 0l) 
		 * 5 float = 9.8f (default = 0.0f)
		 * 6 double = 9.1(default = 0.0)
		 * 7 char = 'a'  (default = '')
		 * 8 boolean = true (default = false)
		 */
		
		//declaration variable
		/*
		 * variable - it is container for value
		 * 
		 * <datatype> variable name = value;
		 */
		
		byte age = 127;
		System.out.println(age);
		
		short dis = 32767;
		System.out.println(dis);
		
		int way = 909600000;
		System.out.println(way);
		
		long sea = 78900000L;
		System.out.println(sea);
		
		float per = 98.9f;
		System.out.println(per);
		
		double perc = 93.2;
		System.out.println(perc);
		
		char c = 'q';
		System.out.println(c);
		
		boolean t = true;
		System.out.println(t);
		
	}
}
